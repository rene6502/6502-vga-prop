; zero page
            .virtual $00
TEMP        .byte ? ; general purpose byte, always assume that subroutines will change this
TEMP_X      .byte ? ; X register backup
TEMP_Y      .byte ? ; Y register backup
POS_X       .byte ? ; x screen position
POS_Y       .byte ? ; y screen position
CUR_X       .byte ? ; current x screen position
CUR_Y       .byte ? ; current y screen position
CHAR        .byte ? ; character
PALETTE     .byte ? ; palette index
TILE_ADDRL  .byte ? ; tile address
TILE_ADDRH  .byte ? ; tile address
SRC_ADDRL   .byte ? ; copy source start address
SRC_ADDRH   .byte ? ; copy source start address
END_ADDRL   .byte ? ; copy source end address
END_ADDRH   .byte ? ; copy source end address
DEST_ADDRL  .byte ? ; copy destination address
DEST_ADDRH  .byte ? ; copy destination address
WAIT_COUNT  .byte ? ; number of 200ms delays

            .endvirtual

start:
        jsr clear_memory
        jsr clear_screen
        jsr init_palette
        
        jsr demo_charset1
        jsr wait
        
        jsr demo_charset2
        jsr wait
        
        jsr demo_colors
        jsr wait
        
        jsr demo_tiles
        jsr wait
        
        rts

        
demo_charset1:
        jsr clear_screen
        lda #1 ; blue on white
        sta PALETTE
        lda #12
        sta POS_X
        lda #7
        sta POS_Y
        jsr draw_charset
        lda #SCREEN_CTRL | SCREEN_TILE
        jsr copy_vram
        rts

demo_charset2:
        lda #0
        sta POS_Y
        lda #0
        sta PALETTE
_next_row:        
        lda #12
        lda #'A'
        clc
        sbc POS_Y
        sta CHAR
        jsr fill_row
        inc POS_Y
        inc PALETTE
        lda PALETTE
        cmp #11
        bne _next_palette
        lda #0
        sta PALETTE
_next_palette:        
        lda POS_Y
        cmp #30
        bne _next_row
        lda #SCREEN_CTRL | SCREEN_TILE
        jsr copy_vram
        rts

; IN  POS_Y        row to render 0..29
; IN  CHAR         first character (incremented after each row)
fill_row:
        jsr calc_tileaddr
        ldy #0
_next_char:        
        lda CHAR
        sta (TILE_ADDRL), y
        inc CHAR
        iny
        lda PALETTE
        sta (TILE_ADDRL), y
        iny
        cpy #40*2
        bne _next_char
        rts             

demo_colors:
        jsr create_colorset
        jsr clear_screen
        lda #12
        sta POS_X
        lda #7
        sta POS_Y
        jsr draw_colors
        lda #SCREEN_CTRL | SCREEN_TILE
        jsr copy_vram
        rts

demo_tiles:
        jsr init_charset
        jsr fill_map
        lda #SCREEN_CTRL | SCREEN_TILE |  SCREEN_CHAR
        jsr copy_vram
        rts

; fill screen with map
fill_map:
        lda #<map
        sta SRC_ADDRL
        lda #>map
        sta SRC_ADDRH
        lda #0
        sta POS_Y
_next_row:        
        jsr calc_srcaddr
        jsr calc_tileaddr
        lda #0
        sta POS_X
_next_col:
        lda POS_X
        asl
        sta TEMP_Y
        lda POS_X
        tay
        lda (SRC_ADDRL), y
        ldy TEMP_Y
        sta (TILE_ADDRL), y
        lda #$1b ; user-defined charset, palette 11
        iny
        sta (TILE_ADDRL), y
        inc POS_X
        lda POS_X
        cmp #40
        bne _next_col
        inc POS_Y
        lda POS_Y
        cmp #29
        bne _next_row
        rts

; calculate address of first tile in row
; IN: POS_Y
; OUT: TILE_ADDRL/H
; MD: A
calc_srcaddr:
        lda POS_Y
        asl A
        tax
        clc
        lda #<map
        adc mul40,x 
        sta SRC_ADDRL ; LSB address of first tile in row
        inx
        lda #>map
        adc mul40,x 
        sta SRC_ADDRH ; LSB address of first tile in row
        rts               

init_charset:
        lda #<tile_charset
        sta SRC_ADDRL
        lda #>tile_charset
        sta SRC_ADDRH
        lda #<tile_charset_end
        sta END_ADDRL
        lda #>tile_charset_end
        sta END_ADDRH
        lda #<CHARSET
        sta DEST_ADDRL
        lda #>CHARSET
        sta DEST_ADDRH
        jsr copy_bytes
        rts                 

create_colorset:
        lda #<CHARSET
        sta DEST_ADDRL
        lda #>CHARSET
        sta DEST_ADDRH
        lda #0
        sta CHAR ; 0..15
_next_char:        
        ldx #32
        ldy #0
        lda CHAR
        asl
        asl
        asl
        asl
        ora CHAR
_next_byte:
        sta (DEST_ADDRL), y
        inc DEST_ADDRL
        bne _skip_inc
        inc DEST_ADDRH
_skip_inc:
        dex
        bne _next_byte
        inc CHAR
        lda CHAR
        cmp #16
        bne _next_char
        lda #SCREEN_CHAR
        jsr copy_vram
        rts        

         
; draws charset at x=POS_X and y=POS_Y using palette index in PALETTE
draw_charset:
        lda #0
        sta CHAR
_next_y:        
        jsr calc_tileaddr
        lda POS_X
        asl A
        tay
        ldx #0
_next_x:        
        lda CHAR
        sta (TILE_ADDRL), y
        iny
        lda PALETTE
        sta (TILE_ADDRL), y
        iny
        inc CHAR
        inx
        cpx #16
        bne _next_x
        inc POS_Y
        lda POS_Y
        cmp #7+16
        bne _next_y
        rts
        

; draws colors at x=POS_X and y=POS_Y
draw_colors:
        lda #0
        sta CUR_X
        sta CUR_Y
_next_y:
        jsr calc_tileaddr
        lda POS_X
        asl
        tay
_next_x:
        lda CUR_X
        lsr
        clc
        adc #0
        sta TEMP
        lda CUR_Y
        lsr
        and #$01
        beq _even
        clc
        lda TEMP
        adc #8
        sta TEMP
_even:  
        lda TEMP      
        sta (TILE_ADDRL), y
        iny
        lda CUR_Y
        lsr
        lsr
        clc
        adc #$1c ; set user-defined charset bit and start palette 12
        sta (TILE_ADDRL), y
        iny
        inc CUR_X
        lda CUR_X
        cmp #16
        bne _next_x
        lda #0
        sta CUR_X
        inc CUR_Y
        inc POS_Y
        lda CUR_Y
        cmp #16
        bne _next_y
        rts        


; draws tile at given position
; POS_X, POS_Y, CHAR, PALETTE
draw_char:
        pha ; save accumulator
        sty TEMP_Y ; save Y register
        jsr calc_tileaddr
        lda POS_X
        asl A       
        tay ; y = POS_X * 2
        lda CHAR
        sta (TILE_ADDRL), y
        iny
        lda PALETTE
        sta (TILE_ADDRL), y
        ldy TEMP_Y ; restore X register
        pla ; restore accumulator 
        rts
        

; calculate address of first tile in row
; IN: POS_Y
; OUT: TILE_ADDRL/H
; MD: A
calc_tileaddr:
        stx TEMP_X ; save X register
        lda POS_Y
        asl A
        tax
        clc
        lda #<TILEMAP
        adc mul80,x 
        sta TILE_ADDRL ; LSB address of first tile in row
        inx
        lda #>TILEMAP
        adc mul80,x 
        sta TILE_ADDRH ; LSB address of first tile in row
        ldx TEMP_X ; restore Y register
        rts

        
; clear screen with ' ' and palette index 0
clear_screen:
        lda #' '
        sta CHAR
        lda #0
        sta PALETTE
        jsr clear_screen_var
        rts
        

; IN  PALETTE
; IN  CHAR
clear_screen_var:
        lda #0
        sta POS_Y
_next_y:                
        jsr calc_tileaddr
        ldy #0
_next_x:
        lda CHAR
        sta (TILE_ADDRL), y
        iny
        lda PALETTE
        sta (TILE_ADDRL), y
        iny
        cpy #40*2
        bne _next_x
        inc POS_Y
        lda POS_Y
        cmp #30
        bne _next_y        
        rts

; IN A copy bits
copy_vram:        
        sta CTL_SCREEN  ; enable copy
        jsr wait_vbl
        lda #SCREEN_NONE ; disable copy
        sta CTL_SCREEN
        rts

wait_vbl:
        lda SIM
        bne _skip ; skip if running in simulation
        lda FRAME
_wait:        
        cmp FRAME
        beq _wait
_skip:        
        rts

; clear complete video ram
clear_memory:
        lda #<VIDEO_START
        sta DEST_ADDRL
        lda #>VIDEO_START
        sta DEST_ADDRH
        ldy #0
_next:          
        lda #$00
        sta (DEST_ADDRL), y
        lda DEST_ADDRL
        cmp #<VIDEO_END
        bne _incr
        lda DEST_ADDRH
        cmp #>VIDEO_END
        bne _incr
        rts
_incr:
        inc DEST_ADDRL ; increment start address
        bne _cont
        inc DEST_ADDRH
_cont:        
        jmp _next
        rts


init_palette:
        lda #<pal0
        sta SRC_ADDRL
        lda #>pal0
        sta SRC_ADDRH
        lda #<pal_end
        sta END_ADDRL
        lda #>pal_end
        sta END_ADDRH
        lda #<PALETTES
        sta DEST_ADDRL
        lda #>PALETTES
        sta DEST_ADDRH
        jsr copy_bytes
        lda #SCREEN_CTRL | SCREEN_TILE | SCREEN_CHAR
        jsr copy_vram
        rts


; IN SRC_ADDRL/H
; IN END_ADDRL/H
; IN DEST_ADDRL/H
; X = count
copy_bytes:
        ldy #0
_next:  
        lda (SRC_ADDRL), y
        sta (DEST_ADDRL), y
        lda SRC_ADDRL
        cmp END_ADDRL
        bne _incr_src
        lda SRC_ADDRH
        cmp END_ADDRH
        bne _incr_src
        rts
_incr_src:        
        inc SRC_ADDRL
        bne _incr_dest
        inc SRC_ADDRH
_incr_dest:
        inc DEST_ADDRL
        bne _next
        inc DEST_ADDRH
        jmp _next
        rts        

        
; wait 20 clock cycles including jsr
wait20clk:
        nop ; CYC=2
        nop ; CYC=2
        nop ; CYC=2
        nop ; CYC=2
        rts ; CYC=12 (jsr+rts)

; wait 100 microseconds including jsr
; changes TEMP
wait100us:
        sty TEMP_Y         ; CYC=3
        ldy #4*CLOCK_MHZ-1 ; CYC=2
_loop:
        jsr wait20clk
        dey         ; CYC=2
        bne _loop   ; CYC=3
        nop         ; CYC=2
        nop         ; CYC=2
        nop         ; CYC=2
        ldy TEMP_Y  ; CYC=4
        rts         ; CYC=12 (jsr+rts)

wait10ms:
        txa
        ldx #100
_loop:
        jsr wait100us
        dex
        bne _loop
        tax
        rts
        
wait200ms:
        stx TEMP_X
        ldx #20
_loop:
        jsr wait10ms
        dex
        bne _loop
        ldx TEMP_X
        rts

wait1s:
        stx TEMP_X
        ldx #200
_loop:
        jsr wait10ms
        dex
        bne _loop
        ldx TEMP_X
        rts

wait:
        lda #0
        sta WAIT_COUNT
_loop:        
        jsr wait1s
        inc WAIT_COUNT
        lda WAIT_COUNT
        cmp #DELAY
        bne _loop
        rts
        

