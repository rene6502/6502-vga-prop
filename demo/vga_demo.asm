VIDEO_START = $0200
VIDEO_END   = $2c7f
CTL_SCREEN  = $0200
TILEMAP     = $0210
PALETTES    = $0b70
FRAME       = $0c70
CHARSET     = $0c80
SIM         = $fff8 ; 1 -> simulation running
SCREEN_NONE = %0000
SCREEN_CTRL = %0001
SCREEN_TILE = %0010
SCREEN_CHAR = %0100
DELAY       = 2

BLACK       = $00
WHITE       = $fc
DARK_GREEN  = $10
GREEN       = $30
BLUE        = $0c
RED         = $c0
DARK_YELLOW = $50
YELLOW      = $f0
